package com.transformer.consts;

/**
 * 符号常量类
 * 
 * @author only
 * @version $Id: StringConst.java
 */
public abstract class StringConst {
	private StringConst(){}
	/**
	 * 等号 "="
	 */
	public static final String EQ = "=";

	/**
	 * url encode后的and "&amp;"
	 */
	public static final String EAND = "&amp;";

	/**
	 * 未做url encode的and "&"
	 */
	public static final String AND = "&";

	/**
	 * 问号 "?"
	 */
	public static final String QMARK = "?";

	/**
	 * 斜杠 "/"
	 */
	public static final String IMARK = "/";

	/**
	 * 分隔符 "-"
	 */
	public static final String SEPARATOR = "-";

	/**
	 * 分号 ";"
	 */
	public static final String SEMICOLON = ";";

	/**
	 * 冒号 ":"
	 */
	public static final String COLON = ":";

	/**
	 * 中文冒号 "："
	 */
	public static final String COLON_ZH = "：";

	/**
	 * 点号 "."
	 */
	public static final String DOT = ".";

	/**
	 * 分页间隔符号 "..."
	 */
	public static final String POINT_SEPARATOR = "...";

	/**
	 * 逗号 ","
	 */
	public static final String COMMA = ",";

	/**
	 * 垂直线 "|"
	 */
	public static final String VERTICAL = "|";

	/**
	 * 空串 ""
	 */
	public static final String EMPTY = "";

	/**
	 * 左中括号 "["
	 */
	public static final String LEFT_M_BRACKET = "[";

	/**
	 * 右中括号 "]"
	 */
	public static final String RIGHT_M_BRACKET = "]";

	/**
	 * 空格 " "
	 */
	public static final String WHITESPACE = " ";

	/**
	 * 下划线 "_"
	 */
	public static final String UNDERLINE = "_";

	/**
	 * 井号 "#"
	 */
	public static final String POUND = "#";

	/**
	 * 异或"^"
	 */
	public static final String XOR = "^";

	/**
	 * 叹号"!"
	 */
	public static final String EMARK = "!";

	/**
	 * @ "@"
	 */
	public static final String AT = "@";

	/**
	 * 波形"~"
	 */
	public static final String WAVE = "~";

	/**
	 * 美元符号"$"
	 */
	public static final String DOLLAR = "$";

	/**
	 * 百分号"%"
	 */
	public static final String PMARK = "%";

	/**
	 * 星号"*"
	 */
	public static final String AMARK = "*";

	/**
	 * 左括号"("
	 */
	public static final String LEFT_BRACKET = "(";

	/**
	 * 右括号")"
	 */
	public static final String RIGHT_BRACKET = ")";

	/**
	 * 左花括号"{"
	 */
	public static final String LEFT_BRACE = "{";

	/**
	 * 右花括号"}"
	 */
	public static final String RIGHT_BRACE = "}";

/**
	 * 左尖括号"<"
	 */
public static final String LEFT_A_BRACKET = "<";

	/**
	 * 右尖括号">"
	 */
	public static final String RIGHT_A_BRACKET = ">";

	/**
	 * 反斜杠"\"
	 */
	public static final String BLANK_SLASH = "\\";

	/**
	 * 单引号"'"
	 */
	public static final String SQUOT = "'";

	/**
	 * 双引号"""
	 */
	public static final String QOUT = "\"";

	/**
	 * 制表符"\t"
	 */
	public static final String TAB = "\t";
}
